    private void enterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterActionPerformed
      SalesRep rep = new SalesRep();
      
      String repId = repIdInput.getText();
      rep.setID(Integer.parseInt(repId));
      
      String firstName = firstNameInput.getText();
      rep.setFirstName(firstName);
      
      String lastName = lastNameInput.getText();
      rep.setLastName(lastName);
      
      String supplies = suppliesSoldInput.getText();
      rep.setSupplies(Double.parseDouble(supplies));
      
      String paper = paperSoldInput.getText();
      rep.setPaper(Double.parseDouble(paper));
      
      String books = booksSoldInput.getText();
      rep.setBooks(Double.parseDouble(books));
      
      String salesDistricts = districtsInput.getText();
      rep.setDistricts("SOUTH");
      
      String meansOfContact = contactPreferenceInput.getText();
      rep.setMeansOfContact("Email");
                         
         
        {  
            try
            {
                FileStorage fs = new FileStorage();
                fs.writeSalesRep(rep,("salesrep.txt"));
            }catch (IOException ex)
            {
            JOptionPane.showMessageDialog(this, "Error Writing to File");
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
          
    }//GEN-LAST:event_enterActionPerformed
         
