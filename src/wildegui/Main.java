    private void evaluateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_evaluateActionPerformed
      SalesReader reader = new SalesReader();
      FileStorage out = new FileStorage();
    try {
        List<SalesRep> rep = reader.readSalesRep("salesrep.txt");
        for (SalesRep sr : rep){
        
            if(sr.getBooks()+ sr.getPaper()+ sr.getSupplies()>8000)
                out.writeSalesRep(sr, "stars.txt");
        
            SalesRepOutput.append(sr.toString());
            SalesRepOutput.append("\n");
            
        }
        
        } catch (IOException ex){
            JOptionPane.showMessageDialog(this, "Unable to open file");
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }

    
        

    }//GEN-LAST:event_evaluateActionPerformed

